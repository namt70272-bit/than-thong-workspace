"""vault_safe_paths -- Blocklist of paths that LLMs must never write to.

Pattern origin: JuliusBrussee/caveman (caveman-compress/scripts/detect.py),
MIT-licensed, 2026-04. Caveman uses a file-extension blocklist to refuse
compressing code/config files. We extend the same idea to vault writes:
some directories and extensions are off-limits to LLM-driven writes
regardless of the request, because corrupting them silently breaks
Obsidian, git, or the local plugin runtime.

Three layers of defense
-----------------------
  1. EXTENSION BLOCKLIST -- inherited from caveman, plus vault additions.
     Anything with these extensions cannot be written by an LLM.
  2. DIRECTORY BLOCKLIST -- vault-specific. .obsidian/, .git/, .trash/
     etc. The plugin owns these; LLM writes here corrupt config or
     destroy version history.
  3. PATH-TRAVERSAL CHECK -- blocks .., ., absolute paths, drive letters.
     Defense-in-depth; the WebSocket handler should reject these too.

Usage
-----
    from vault_safe_paths import is_safe_to_write

    if not is_safe_to_write(path):
        raise PermissionError(f"refusing LLM write to protected path: {path}")
    # ... proceed with write
"""

from __future__ import annotations

from pathlib import PurePosixPath

__all__ = [
    "BLOCKED_EXTENSIONS",
    "BLOCKED_DIRECTORIES",
    "ALLOWED_VAULT_EXTENSIONS",
    "is_safe_to_write",
    "is_blocked_extension",
    "is_blocked_directory",
]


# ---------- Caveman-inherited extension blocklist ----------
# Source: caveman-compress/scripts/detect.py SKIP_EXTENSIONS
# These are code/config files. Caveman skips them for compression;
# we refuse to LLM-write them in the vault.

_CAVEMAN_SKIP_EXTENSIONS: frozenset[str] = frozenset({
    ".py", ".js", ".ts", ".tsx", ".jsx",
    ".json", ".yaml", ".yml", ".toml",
    ".env", ".lock",
    ".css", ".scss", ".html", ".xml",
    ".sql", ".sh", ".bash", ".zsh",
    ".go", ".rs", ".java",
    ".c", ".cpp", ".h", ".hpp",
    ".rb", ".php", ".swift", ".kt", ".lua",
    ".dockerfile", ".makefile",
    ".csv", ".ini", ".cfg",
})

# ---------- Vault-specific extension additions ----------
# Binary, executable, and credential extensions that should never be
# generated by an LLM at all, even if the path is in the vault.

_VAULT_EXTRA_BLOCKED: frozenset[str] = frozenset({
    # Binaries / executables
    ".exe", ".dll", ".so", ".dylib", ".bin",
    # Compressed archives (LLM cannot meaningfully generate these)
    ".zip", ".tar", ".gz", ".bz2", ".xz", ".7z", ".rar",
    # Images (LLM cannot generate raster bytes via text channel)
    ".png", ".jpg", ".jpeg", ".gif", ".webp", ".bmp", ".ico", ".svg",
    # Audio / video
    ".mp3", ".mp4", ".wav", ".flac", ".webm", ".mov", ".avi", ".mkv",
    # Documents (binary)
    ".pdf", ".docx", ".xlsx", ".pptx",
    # Credentials / secrets
    ".pem", ".key", ".crt", ".pfx", ".p12", ".gpg", ".asc",
    # Database files
    ".db", ".sqlite", ".sqlite3", ".duckdb",
    # Git internals
    ".pack", ".idx",
})

BLOCKED_EXTENSIONS: frozenset[str] = _CAVEMAN_SKIP_EXTENSIONS | _VAULT_EXTRA_BLOCKED

# Vault is markdown-first. These are the safe extensions LLMs can write.
ALLOWED_VAULT_EXTENSIONS: frozenset[str] = frozenset({
    ".md", ".markdown", ".txt", ".rst",
    # Frontmatter-only files used by some Obsidian plugins.
    ".canvas",  # Obsidian Canvas JSON files -- gated separately, see below
})

# ---------- Directory blocklist ----------
# Path components (anywhere in the path) that mark protected directories.
# Match is exact-segment, not substring.

BLOCKED_DIRECTORIES: frozenset[str] = frozenset({
    # Obsidian config and plugin internals
    ".obsidian",
    ".obsidian.bak",
    # Git internals
    ".git",
    ".gitea",
    ".github",
    # Trash and cache
    ".trash",
    ".cache",
    ".tmp",
    "node_modules",
    "__pycache__",
    # Vault-bridge / vault-mind internals
    ".vault-bridge",
    ".vault-mind",
    # GSD planning (separate workflow, not LLM-writable from vault path)
    ".planning",
    # FSC internals
    ".fsc",
    # Memory keeper persistent state
    ".memory_keeper",
})


# ---------- Checks ----------


def is_blocked_extension(path: str) -> bool:
    """True if the file extension is on the blocklist (Caveman + vault extras)."""
    name = PurePosixPath(path.replace("\\", "/")).name
    suffix = PurePosixPath(path.replace("\\", "/")).suffix.lower()
    # P2 fix: bare dotfiles like ".env" have suffix "" in pathlib (leading dot
    # is treated as the stem, not the extension). Special-case: if the filename
    # starts with "." and contains no further ".", treat the whole name as a
    # dotfile key and check whether "." + name[1:] is in the blocklist.
    if not suffix:
        if name.startswith(".") and "." not in name[1:]:
            return ("." + name[1:].lower()) in BLOCKED_EXTENSIONS
        return False
    return suffix in BLOCKED_EXTENSIONS


def is_blocked_directory(path: str) -> bool:
    """True if any path segment matches a blocked directory."""
    parts = PurePosixPath(path.replace("\\", "/")).parts
    for part in parts:
        if part in BLOCKED_DIRECTORIES:
            return True
    return False


def _normalize_vault_path(path: str) -> str:
    """Normalize a vault-relative path to match the TypeScript handler.

    The TS handler at src/handlers.ts:19-22 strips leading ``./``, collapses
    consecutive ``/`` separators, and drops bare ``.`` segments before
    forwarding the path to the safety gate.  This function replicates that
    normalization so both layers agree on borderline paths.

    Absolute paths and ``..`` segments are left intact so that the caller
    (``_has_path_traversal``) can still detect and reject them.
    """
    norm = path.replace("\\", "/")
    # Strip a single leading "./" (sloppy LLM clients often emit these).
    if norm.startswith("./"):
        norm = norm[2:]
    # Collapse consecutive slashes.
    while "//" in norm:
        norm = norm.replace("//", "/")
    # Drop bare "." segments that appear anywhere in the path (e.g. "a/./b").
    # We keep ".." so that _has_path_traversal can still reject it.
    parts = norm.split("/")
    parts = [p for p in parts if p != "."]
    norm = "/".join(parts)
    return norm


def _has_path_traversal(path: str) -> bool:
    """True if the path contains traversal markers or absolute prefixes."""
    # Normalize first so that "." segments are stripped before checking.
    norm = _normalize_vault_path(path)
    if norm.startswith("/"):
        return True
    # Windows drive letter (C:, D:, ...).
    if len(norm) >= 2 and norm[1] == ":":
        return True
    parts = norm.split("/")
    return any(p == ".." for p in parts)


def is_safe_to_write(path: str, *, allow_canvas: bool = False, allow_extensionless: bool = False) -> bool:
    """Decide if an LLM-driven write to this path is safe.

    Args:
        path: The relative vault path. Forward or back slashes accepted.
        allow_canvas: If True, .canvas files are allowed. Default False
            because Obsidian Canvas JSON is binary-fragile and one bad
            write can corrupt the entire canvas. Enable only if you
            have a Canvas-aware writer.
        allow_extensionless: If True, extensionless paths are allowed.
            Use for vault_mkdir where directory names never have extensions.

    Returns:
        True iff the path is safe for LLM-driven write. False otherwise.

    Examples:
        is_safe_to_write("notes/idea.md")        -> True
        is_safe_to_write(".obsidian/config.json") -> False  (blocked dir)
        is_safe_to_write("scripts/run.py")       -> False  (blocked ext)
        is_safe_to_write("../secrets.md")        -> False  (traversal)
        is_safe_to_write("notes/draft.canvas")   -> False  (canvas gated)
    """
    if not path or not path.strip():
        return False
    # Normalize first so that "." segments and leading "./" are stripped
    # before any check runs.  _has_path_traversal also calls
    # _normalize_vault_path internally; we normalize here so the directory
    # and extension checks also see the cleaned path.
    normalized = _normalize_vault_path(path)
    if _has_path_traversal(path):
        return False
    if is_blocked_directory(normalized):
        return False
    if is_blocked_extension(normalized):
        return False
    suffix = PurePosixPath(normalized).suffix.lower()
    if suffix == ".canvas" and not allow_canvas:
        return False
    # P3 fix: extensionless files (Makefile, README, binary blobs) must be
    # refused. The markdown-first intent means only known-safe extensions pass.
    # Bare dotfiles (.env) are handled above by is_blocked_extension P2 fix
    # and will already return False before reaching this point.
    # allow_extensionless=True is used by vault_mkdir where directory names
    # never carry file extensions.
    if not suffix:
        if allow_extensionless:
            return True
        return False
    if suffix not in ALLOWED_VAULT_EXTENSIONS:
        # Unknown extension -- err on the side of safety.
        return False
    return True
